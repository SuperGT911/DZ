# Каково количество чисел из интервала [10;99]?
# Вводим количество чисел в последовательности
N = int(input("Введите количество чисел: "))

# Инициализируем переменную для подсчета
count_in_range = 0

# Вводим числа и проверяем, попадают ли они в интервал [10;99]
for i in range(N):
    num = int(input(f"Введите число {i + 1}: "))
    if 10 <= num <= 99:
        count_in_range += 1

print(f"Чисел из интервала [10;99]: {count_in_range}")
